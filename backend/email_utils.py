import smtplib
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart
from email.mime.application import MIMEApplication
import os
import io
from dotenv import load_dotenv
from fpdf import FPDF

load_dotenv()

# We load environment variables for the SMTP credentials.
# If these are not configured, the emails will just print to console.
SMTP_SERVER = os.getenv("SMTP_SERVER", "smtp.gmail.com")
SMTP_PORT = int(os.getenv("SMTP_PORT", 587))
SMTP_USERNAME = os.getenv("SMTP_USERNAME", "")
SMTP_PASSWORD = os.getenv("SMTP_PASSWORD", "")

def send_email(to_email: str, subject: str, message_body: str, attachment: bytes = None, attachment_name: str = None):
    if not SMTP_USERNAME:
        print("ATTENTION: SMTP_USERNAME is empty, cannot send real email.")
        return

    msg = MIMEMultipart()
    msg['From'] = SMTP_USERNAME
    msg['To'] = to_email
    msg['Subject'] = subject

    msg.attach(MIMEText(message_body, 'plain'))
    
    if attachment and attachment_name:
        part = MIMEApplication(attachment, Name=attachment_name)
        part['Content-Disposition'] = f'attachment; filename="{attachment_name}"'
        msg.attach(part)

    try:
        server = smtplib.SMTP(SMTP_SERVER, SMTP_PORT)
        server.starttls()
        # Login with email and app password
        server.login(SMTP_USERNAME, SMTP_PASSWORD)
        server.send_message(msg)
        server.quit()
        print(f"Successfully sent SMTP email to {to_email}")
    except Exception as e:
        print(f"Failed to send email via SMTP: {e}")
        # Reraise so the API fails if SMTP is broken.
        raise e

