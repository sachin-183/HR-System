def generate_shellkode_offer_pdf(name: str, job_title: str) -> bytes:
    pdf = FPDF()
    pdf.add_page()
    
    # Place the ShellKode Logo in the top-right corner
    logo_path = os.path.join(os.path.dirname(__file__), "shellkode_logo.jpg")
    if os.path.exists(logo_path):
        pdf.image(logo_path, x=130, y=10, w=70) # Accommodates the wider 669x177 image
    
    # Push content down so it starts below the logo height
    pdf.ln(30)
    
    # Title
    pdf.set_font("helvetica", "B", 18)
    pdf.set_text_color(2, 173, 239) # #02ADEF Cyan
    pdf.cell(0, 10, "OFFICIAL OFFER OF EMPLOYMENT", new_x="LMARGIN", new_y="NEXT", align="C")
    
    # Body
    pdf.ln(15)
    pdf.set_font("helvetica", "", 12)
    pdf.set_text_color(50, 50, 50)
    
    body_text = f"Dear {name},\n\n" \
                f"We are extremely pleased to formally offer you the position of {job_title} at ShellKode. " \
                f"Throughout the interview process, we were very impressed by your skills, background, and alignment with our company values.\n\n" \
                f"We believe you will make a fantastic addition to the team and help us build the future of technology.\n\n" \
                f"Please reply back to the attached email indicating whether you formally Accept or Reject this offer so we may begin the final onboarding procedures.\n\n" \
                f"Sincerely,\n" \
                f"The Executive HR Team\nShellKode"
                
    pdf.multi_cell(0, 8, body_text)
    
    # Footer
    pdf.set_y(-30)
    pdf.set_font("helvetica", "I", 10)
    pdf.set_text_color(150, 150, 150)
    pdf.multi_cell(0, 5, "This is an automated formal offer document generated by the ShellKode HR platform.", align="C")
    
    # Export to bytes instead of file
    return bytes(pdf.output())

